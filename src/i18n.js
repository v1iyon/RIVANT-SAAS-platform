function getDict(lang) {
  return DICT[lang] || DICT.EN;
}

function formatMoney(value, lang) {
  const d = getDict(lang);
  try {
    return new Intl.NumberFormat(d.locale, { style: "currency", currency: d.currency, maximumFractionDigits: 0 }).format(value ?? 0);
  } catch {
    return `${value ?? 0} ${d.currency}`;
  }
}

module.exports = { DICT, getDict, formatMoney };